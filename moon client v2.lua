local themeNames = {}
for _, t in ipairs(themes) do
    table.insert(themeNames, t[1])
end

local currentThemeIndex = 1

Window:CreateTopbarButton("ThemeSwitcher", "palette", function()
    currentThemeIndex = currentThemeIndex % #themeNames + 1
    local nextTheme = themeNames[currentThemeIndex]
    WindUI:SetTheme(nextTheme)
    UpdateOpenButton(nextTheme)
    WindUI:Notify({
        Title    = "Theme Changed",
        Content  = "Now using: " .. nextTheme,
        Icon     = "palette",
        Duration = 2,
    })
end, 989)

