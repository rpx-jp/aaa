local function UpdateOpenButton(themeName)
    local g = themeGradients[themeName] or themeGradients["Coffee"]
    Window:EditOpenButton({
        Title           = "Open Moon Client",
        Icon            = "moon",
        CornerRadius    = UDim.new(0, 18),
        StrokeThickness = 2,
        Color           = ColorSequence.new(
            Color3.fromHex(g[1]),
            Color3.fromHex(g[2])
        ),
        OnlyMobile = false,
        Enabled    = true,
        Draggable  = true,
    })
end

